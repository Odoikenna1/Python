# Use the remainder operator

x = 1024

y = 2

if x % 4 == 0:

	print(f"{x} is a multiple of 4")

else:

	print(f"{x} is not a multiple of 4")

if y % 10 == 0:

	print(f"{y} is a multiple of 10")

else:

	print(f"{y} is not a multiple of 10")
