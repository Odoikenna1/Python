#objective: find and display the;
#diameter, circumference, and area

#VARIABLES


PI = 3.14159

circumference = 2 * PI

radius = circumference / 2 * 3.14159

diametere = 2 * radius

area = PI * radius ** 2

print(f"The diameter is {diametere}\nThe circumference is {circumference}\nThe area is {area}")

